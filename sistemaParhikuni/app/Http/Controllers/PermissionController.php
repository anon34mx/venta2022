    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Permission $permission)
    {
        // dd($permission->roles);
        if(sizeof($permission->roles) <= 0){
            $permission->delete();
            app()->make(\Spatie\Permission\Models\Permission::class)->forgetCachedPermissions();
            return back()->with('status', 'Permiso eliminado.');
        }else{
            return back()->withErrors([
                'No se puede borrar el permiso, está asignado a roles'
            ]);
        }
    }
